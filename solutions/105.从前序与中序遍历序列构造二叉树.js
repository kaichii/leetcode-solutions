/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {number[]} preorder
 * @param {number[]} inorder
 * @return {TreeNode}
 */
var buildTree = function (preorder, inorder) {
  const inorderMap = new Map();

  inorder.forEach((i, index) => {
    inorderMap.set(i, index);
  });

  function build(preorder, pS, pE, inorder, iS, iE) {
    if (pS > pE) return null;

    const index = inorderMap.get(preorder[pS]);

    const size = index - iS;

    return new TreeNode(
      preorder[pS],
      build(preorder, pS + 1, pS + size, inorder, iS, index - 1),
      build(preorder, pS + size + 1, pE, inorder, index + 1, iE)
    );
  }

  return build(
    preorder,
    0,
    preorder.length - 1,
    inorder,
    0,
    inorder.length - 1
  );
};
